            <p>Fibres are converted into yarn through a variety of processes, which typically involve a cleaning stage to remove any impurities (natural fibres usually require more pre-cleaning than man-made fibres), followed by spinning or twisting of the fibres. The fibres are processed either in their natural form or blended with synthetic fibres to produce a yarn according to the desired characteristics.</p>
