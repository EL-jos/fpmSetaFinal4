            <h3>How is clothing manufactured?</h3>
            <p>The apparel (clothing) manufacturing industry transforms fabrics produced by textile manufacturers into clothing and accessories that supply the retail stores. By cutting and sewing fabrics or other materials, such as leather, rubberised fabrics, plastics, and furs, workers in this industry help to keep consumers warm, dry, and in style. The apparel industry consists mostly of production workers who perform the cutting and sewing functions in an assembly line. Computers and computer-controlled equipment aid in many functions, such as design, pattern making, and cutting; however, many functions (especially the sewing function) are usually carried out manually by machinists. A typical process of creating a finished garment based on a design is illustrated in Figure 4.</p>
            <h3>Apparel Manufacture Process</h3>
            <figure>
                <img src="assets/images/apparel_manufacture_process.jpeg" alt="" class="img">
